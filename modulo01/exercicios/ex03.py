# Escreva um programa Java que calcule a média de três números inteiros.

nota_01 = int(input('Informe a sua primeira nota: '))
nota_02 = int(input('Informe a sua segunda nota: '))
nota_03 = int(input('Informe a sua terceira nota: '))

media = (nota_01 + nota_02 + nota_03) / 3

print(f'A média do aluno é {media}.')
